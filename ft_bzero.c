#include "libft.h"

void	*ft_bzero(void *ptr, size_t n)
{
	size_t	i;
	char	*s;

	i = 0;
	s = ptr;
	while (i < n)
	{
		s[i] = 0;
		i++;
	}
	return (ptr);
}
